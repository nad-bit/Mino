    static var currentLanguage: String {
        let defaultLang = "en"
        var langCode = defaultLang
        
        if #available(macOS 13, *) {
            langCode = Locale.current.language.languageCode?.identifier ?? defaultLang
        } else {
            langCode = Locale.current.languageCode ?? defaultLang
        }
        
        // Handle specialized language variants (e.g. zh-Hans -> zh)
        if langCode.hasPrefix("zh") {
            return "zh"
        }
        
        return i18n.keys.contains(langCode) ? langCode : defaultLang
    }
    
    static func get(_ key: String) -> String {
        let lang = currentLanguage
        if let translated = i18n[lang]?[key] {
            return translated
        }
        return i18n["en"]?[key] ?? key
    }
